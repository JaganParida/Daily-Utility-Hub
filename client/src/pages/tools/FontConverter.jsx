const convertText = (text, map) => {
  return text.split('').map(char => map[char] || char).join('');
};

const FontConverter = () => {
  const [text, setText] = useState('Stylish Text');
  const [copiedId, setCopiedId] = useState(null);

  const fonts = [
    { id: 'bold', name: 'Bold', map: fontMaps.bold },
    { id: 'italic', name: 'Italic', map: fontMaps.italic },
    { id: 'boldItalic', name: 'Bold Italic', map: fontMaps.boldItalic },
    { id: 'script', name: 'Cursive / Script', map: fontMaps.script },
    { id: 'boldScript', name: 'Bold Cursive', map: fontMaps.boldScript },
    { id: 'fraktur', name: 'Gothic / Fraktur', map: fontMaps.fraktur },
    { id: 'monospace', name: 'Monospace', map: fontMaps.monospace },
    { id: 'doubleStruck', name: 'Double Struck', map: fontMaps.doubleStruck },
    { id: 'circled', name: 'Circled', map: fontMaps.circled },
    { id: 'squared', name: 'Squared', map: fontMaps.squared },
  ];

  const handleCopy = (convertedText, id) => {
    navigator.clipboard.writeText(convertedText);
    setCopiedId(id);
    toast.success('Font copied to clipboard!');
    setTimeout(() => setCopiedId(null), 2000);
  };

  return (
    <div className="max-w-4xl mx-auto flex flex-col min-h-[calc(100vh-140px)]">
      <div className="mb-6 flex items-center gap-3 shrink-0">
        <div className="p-2 bg-rose-500/10 text-rose-500 rounded-lg shadow-sm">
          <Type size={28} />
        </div>
        <div>
          <h1 className="text-2xl font-bold tracking-tight text-foreground">Stylish Font Converter</h1>
          <p className="text-muted-foreground mt-1 text-sm">Convert regular text into aesthetic fonts for Instagram, Twitter, and TikTok.</p>
        </div>
      </div>

      <div className="bg-card border border-border rounded-2xl shadow-sm overflow-hidden flex flex-col">
        {/* Input Area */}
        <div className="p-4 sm:p-6 border-b border-border bg-muted/20">
          <label className="text-xs font-bold uppercase tracking-wider text-muted-foreground mb-2 block">Your Text</label>
          <input
            type="text"
            className="w-full bg-background border border-border rounded-xl px-4 py-3 text-lg font-medium text-foreground placeholder:text-muted-foreground focus:outline-none focus:border-rose-500/50 focus:ring-2 focus:ring-rose-500/20 transition-all"
            placeholder="Type something cool here..."
            value={text}
            onChange={(e) => setText(e.target.value)}
            spellCheck="false"
          />
        </div>

        {/* Results Area */}
        <div className="p-4 sm:p-6 flex-1 overflow-y-auto custom-scrollbar">
          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            {fonts.map((font) => {
              const convertedText = convertText(text || 'Stylish Text', font.map);
              const isCopied = copiedId === font.id;
              
              return (
                <div 
                  key={font.id} 
                  className="bg-muted/30 border border-border rounded-xl p-4 flex flex-col gap-2 hover:border-rose-500/30 transition-colors group cursor-pointer"
                  onClick={() => handleCopy(convertedText, font.id)}
                >
                  <div className="flex justify-between items-center">
                    <span className="text-xs font-semibold text-muted-foreground uppercase tracking-wider">{font.name}</span>
                    <button className="text-muted-foreground group-hover:text-rose-500 transition-colors">
                      {isCopied ? <Check size={14} className="text-green-500" /> : <Copy size={14} />}
                    </button>
                  </div>
                  <div className="text-xl text-foreground break-all">
                    {convertedText}
                  </div>
                </div>
              );
            })}
          </div>
        </div>
      </div>
    </div>
  );
};

export default FontConverter;
